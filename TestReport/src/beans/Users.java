package beans;

//买家类
public class Users {
	private int usersId;//买家ID
	private String usersName;//买家姓名
	private int usersAge;//买家年龄
	private String sex;//买家性别
	private String usersCity;//买家所在地区
	private int usersPhone;//买家电话
	public int getUsersId() {
		return usersId;
	}
	public void setUsersId(int usersId) {
		this.usersId = usersId;
	}
	public String getUsersName() {
		return usersName;
	}
	public void setUsersName(String usersName) {
		this.usersName = usersName;
	}
	public int getUsersAge() {
		return usersAge;
	}
	public void setUsersAge(int usersAge) {
		this.usersAge = usersAge;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getUsersCity() {
		return usersCity;
	}
	public void setUsersCity(String usersCity) {
		this.usersCity = usersCity;
	}
	public int getUsersPhone() {
		return usersPhone;
	}
	public void setUsersPhone(int usersPhone) {
		this.usersPhone = usersPhone;
	}
	

}
